#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	cnt;
	size_t	i;

	cnt = 0;
	i = 0;
	while (src[cnt])
		++cnt;
	if (dstsize == 0)
		return (cnt);
	while (src[i] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[i];
		++i;
	}
	dst[i] = '\0';
	return (cnt);
}
