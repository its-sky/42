#include "../../include/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new_node)
{
	t_list	*p;

	p = *lst;
	if (p)
	{
		while (p->next)
			p = p->next;
		p->next = new_node;
	}
	else
	{
		*lst = new_node;
	}
}
